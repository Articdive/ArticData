package com.minestom.data_generator;

@SuppressWarnings({"FieldCanBeLocal", "unused"})
public final class GeneratedEffect {
    private final String id;
    private final String descriptionId;
    private final int color;
    private final boolean instantenous;

    public GeneratedEffect(String id, String descriptionId, int color, boolean instantenous) {
        this.id = id;
        this.descriptionId = descriptionId;
        this.color = color;
        this.instantenous = instantenous;
    }
}
