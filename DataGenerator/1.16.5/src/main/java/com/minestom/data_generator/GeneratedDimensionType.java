package com.minestom.data_generator;

@SuppressWarnings({"FieldCanBeLocal", "unused"})
public class GeneratedDimensionType {
    private final String id;
    private final boolean bedWorks;
    private final double coordinateScale;
    private final boolean hasCeiling;
    private final boolean hasFixedTime;
    private final boolean hasRaids;
    private final boolean hasSkyLight;
    private final boolean piglinSafe;
    private final int logicalHeight;
    private final boolean natural;
    private final boolean ultraWarm;
    private final boolean respawnAnchorWorks;

    public GeneratedDimensionType(String id, boolean bedWorks, double coordinateScale, boolean hasCeiling, boolean hasFixedTime, boolean hasRaids, boolean hasSkyLight, boolean piglinSafe, int logicalHeight, boolean natural, boolean ultraWarm, boolean respawnAnchorWorks) {
        this.id = id;
        this.bedWorks = bedWorks;
        this.coordinateScale = coordinateScale;
        this.hasCeiling = hasCeiling;
        this.hasFixedTime = hasFixedTime;
        this.hasRaids = hasRaids;
        this.hasSkyLight = hasSkyLight;
        this.piglinSafe = piglinSafe;
        this.logicalHeight = logicalHeight;
        this.natural = natural;
        this.ultraWarm = ultraWarm;
        this.respawnAnchorWorks = respawnAnchorWorks;
    }
}
